; Heizungssteuerung
CSEG AT 0H
LJMP HEIZUNG

; Eingabevektor
MANUELL EQU P2.0
SENSOR EQU P2.1
TEMPARATURE EQU P2.2

; Ausgabevektor
HEIZUNG EQU P3.4

; Prgramm
HEIZUNG:
	MOV C, SENSOR
	ANL C, TEMPARATURE
	ORL C, MANUELL
	JNC HEIZUNG_AUS
	JMP HEIZUNG_AN

HEIZUNG_AUS:
	SETB P3.4
	JMP HEIZUNG

HEIZUNG_AN:
	CLR P3.4
	JMP HEIZUNG